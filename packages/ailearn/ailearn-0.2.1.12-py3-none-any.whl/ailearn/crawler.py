def get_html(url, headers=None):
    if headers is not None:
        html = requests.get(url, headers=headers)
    else:
        html = requests.get(url, headers={'User-Agent': random.choice(ualist)})
    html.encoding = 'utf-8'
    return html


def get_soup(html, features='html.parser'):
    '''

    :param html: class:`Response <Response>` object
    :param features: lxml, html.parser or html5lib
    :return:
    '''
    obj = BeautifulSoup(html.text, features=features)
    return obj


def get_soup_from_url(url, headers=None, features='html.parser'):
    return get_soup(get_html(url, headers=headers), features=features)
